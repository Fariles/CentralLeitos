    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        numTentativas ++;
        
        Usuario usuario;
        
        try {
            usuario = (Usuario) em.createQuery("SELECT u FROM Usuario u WHERE u.login = :login")
                                  .setParameter("login", txtLogin.getText())
                                  .getSingleResult();
        } catch(NoResultException e) {
            usuario = null;
        }
        
        if(usuario != null && usuario.getSenha().equals(txtSenha.getText())){
            new MenuInicial().setVisible(true);
            this.setVisible(false);
            
            autenticado = true;
            
        }
            
        else if(numTentativas >= 3) {
            JOptionPane.showMessageDialog(this, "M치ximo de tentativas atingidas, o sistema ser치 finalizado!");
            autenticado = false;
            System.exit(0);
        } 
        else {
            JOptionPane.showMessageDialog(this, "Usu치rio ou Senha incorretos, tente novamente!");
            txtSenha.requestFocus(); 
        }
    }//GEN-LAST:event_jButton1MouseClicked

